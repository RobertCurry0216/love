function love.draw()
  for y=1,#tilemap do
    for x=1,#tilemap[y] do
      if tilemap[y][x] == 1 then
        love.graphics.draw(
          tiles, block, (x-1)*16, (y-1)*16
        )
      end
    end
  end
end